#include "libft.h"
/* ************************************************************************** */
/* STRING_LENTH - count the lenth of the pointer and returns.                 */
/* ************************************************************************** */
size_t	ft_strlen(const char *s)
{
	size_t	count;

	count = 0;
	while (s[count])
		count++;
	return (count);
}
